package oop;

public class While_DoWhile {

	public static void main(String[] args) {
		
		int a = 1, b = 0;
		int c = 1, d = 0;
		while(a <= 10) {
			a++;
			b++;
			System.out.println(b);
		}
		
		//Do While
		
		do {
			d++;
			c++;
			System.out.println(c+1);
		}while(d <= 5);
		
	
	}

}
